void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{

	uint16_t *sensBuffer = (uint16_t*)sensValues;
	int32_t averagedVoltages[NUMBER_OF_CHANNELS] ;

	for (uint8_t i = 0; i < sizeof(averagedVoltages)/sizeof(averagedVoltages[0]); i++)
	{
		averagedVoltages[i] = 0;
	}

	for (uint8_t i = 0; i < ADC_AVERAGE; i++)
	{
		for (uint8_t j = 0; j < NUMBER_OF_CHANNELS; j++){
			averagedVoltages[j] += *sensBuffer++;
		}
	}

	for (int i = 0; i < NUMBER_OF_CHANNELS; i++)
	{
		 averagedVoltages[i] = (averagedVoltages[i] * BRD_VOLTAGE / ADC_AVERAGE) / 4096;
	}

	potentiometerVolt  =  averagedVoltages[0];
	externalSensorTemp = (-averagedVoltages[1] * 100) / 200 + 990 ;
	internalSensorTemp = ((averagedVoltages[2] - 760) * 100  / 25) + 250;

	chkWrnLimits(); // count number of warnings
	pwmPeriod(); // set PWM period on each LED

}


void chkWrnLimits()
{

	if(potentiometerVolt > POTENTIOMETER_LIMIT)
		potAlarmFlag = 1;
	else
		potAlarmFlag = 0;
	if(externalSensorTemp > TEMPERATURE_LIMIT)
		extSensorAlarmFlag = 1;
	else
		extSensorAlarmFlag = 0;
	if(internalSensorTemp > TEMPERATURE_LIMIT)
		intSensorAlarmFlag = 1;
	else
		intSensorAlarmFlag = 0;
	return;

}

void pwmPeriod()
{
	int32_t pulseLength;

	pulseLength = PROPORTIONAL_PWM_PERIOD(potentiometerVolt, POTENTIOMETER_MIN_VALUE, POTENTIOMETER_MAX_VALUE);
	TIM4->CCR4 = PWM_LIMIT(pulseLength);

	pulseLength = PROPORTIONAL_PWM_PERIOD(externalSensorTemp, TEMPERATURE_MIN_VALUE, TEMPERATURE_MAX_VALUE);
	TIM4->CCR1 = PWM_LIMIT(pulseLength);

	pulseLength = PROPORTIONAL_PWM_PERIOD(internalSensorTemp, TEMPERATURE_MIN_VALUE, TEMPERATURE_MAX_VALUE);
	TIM4->CCR2 = PWM_LIMIT(pulseLength);
	return;

}



void alarmLed()
{

	uint8_t wrnVal = potAlarmFlag + extSensorAlarmFlag + intSensorAlarmFlag;

	if(wrnVal == 0)
	{
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);
	}
	else if(prevWrnState == 0)
	{

		timerVal = __HAL_TIM_GET_COUNTER(&htim14);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET);
	}
	else if(wrnVal == 1)
	{

		if (__HAL_TIM_GET_COUNTER(&htim14) - timerVal >= 510)
		{
			timerVal = __HAL_TIM_GET_COUNTER(&htim14);
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);
		}
	}
	else if (wrnVal == 2)
	{

		if (__HAL_TIM_GET_COUNTER(&htim14) - timerVal >= 200)
		{
			timerVal = __HAL_TIM_GET_COUNTER(&htim14);
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);
		}
	}
	else if (__HAL_TIM_GET_COUNTER(&htim14) - timerVal >= 100)
	{
			timerVal = __HAL_TIM_GET_COUNTER(&htim14);
			HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);
	}
	prevWrnState = wrnVal;

}
